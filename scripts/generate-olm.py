def main() -> None:
    parser = argparse.ArgumentParser(
        description="Generate OLM bundle manifests for the Stackable Secret Operator",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument(
        "--version",
        "-v",
        required=True,
        metavar="VERSION",
        help="Operator version string, e.g. 26.3.0",
    )
    parser.add_argument(
        "--openshift-versions",
        metavar="RANGE",
        default="v4.18-v4.21",
        help="Value for com.redhat.openshift.versions (default: v4.18-v4.21)",
    )
    parser.add_argument(
        "--output-dir",
        "-o",
        metavar="DIR",
        default=str(OLM_OUTPUT),
        help=(
            "Directory that already exists where the OLM bundle will be written."
            " A sub-folder named after the operator version is created inside it."
            f" (default: {OLM_OUTPUT.relative_to(REPO_ROOT)})"
        ),
    )
    args = parser.parse_args()

    version: str = args.version
    openshift_versions: str = args.openshift_versions
    olm_output = Path(args.output_dir).resolve()
    if not olm_output.is_dir():
        parser.error(f"--output-dir does not exist or is not a directory: {olm_output}")
    ver_parts = version.split(".")
    channel = f"{ver_parts[0]}.{ver_parts[1]}" if len(ver_parts) >= 2 else version

    output_dir = olm_output / version

    print(f"\n=== Generating OLM bundle for secret-operator v{version} ===\n")

    # ------------------------------------------------------------------
    # 1. Load values.yaml
    # ------------------------------------------------------------------
    print("[1/4] Reading Helm values.yaml ...")
    values = load_values()
    sidecars = sidecar_image_info(values)
    main_oci_repo: str = values["image"]["repository"]
    main_quay_repo = OCI_TO_QUAY[main_oci_repo]

    # ------------------------------------------------------------------
    # 2. Look up quay.io image digests
    # ------------------------------------------------------------------
    print("[2/4] Resolving image digests on quay.io ...")

    print(f"  {main_quay_repo}:{version}")
    main_digest = get_quay_digest(main_quay_repo, version)
    print(f"    -> {main_digest}")

    final_images: dict[str, str] = {
        "secret-operator": f"{main_quay_repo}@{main_digest}",
    }
    # image_map: OCI repo:tag  ->  quay.io@digest  (for textual substitution)
    image_map: dict[str, str] = {
        f"{main_oci_repo}:{version}": final_images["secret-operator"],
    }

    for sidecar_name, info in sidecars.items():
        quay_repo = OCI_TO_QUAY[info["repo"]]
        tag = info["tag"]
        print(f"  {quay_repo}:{tag}")
        digest = get_quay_digest(quay_repo, tag)
        print(f"    -> {digest}")
        ref = f"{quay_repo}@{digest}"
        final_images[sidecar_name] = ref
        image_map[f"{info['repo']}:{tag}"] = ref

    # ------------------------------------------------------------------
    # 3. Render Helm chart
    # ------------------------------------------------------------------
    print("[3/4] Rendering Helm chart ...")
    helm_docs = render_helm(version)
    print(f"  -> {len(helm_docs)} Kubernetes resources")

    # ------------------------------------------------------------------
    # 4. Write output files
    # ------------------------------------------------------------------
    print(f"[4/4] Writing OLM bundle to {output_dir} ...")

    # tag_map: quay.io@digest -> human tag (for inline ``# <tag>`` comments)
    tag_map: dict[str, str] = {
        final_images["secret-operator"]: version,
        final_images["csi-provisioner"]: sidecars["csi-provisioner"]["tag"],
        final_images["csi-node-driver-registrar"]: sidecars[
            "csi-node-driver-registrar"
        ]["tag"],
    }

    # Deployer ConfigMap
    configmap = build_configmap(helm_docs, image_map, tag_map)
    write_yaml(output_dir / "manifests" / "secret-operator-manifests.yaml", configmap)

    # ClusterServiceVersion
    csv = build_csv(
        version, helm_docs, final_images, _STACKABLE_ICON_B64, openshift_versions
    )
    csv_filename = f"stackable-secret-operator.v{version}.clusterserviceversion.yaml"
    csv_path = output_dir / "manifests" / csv_filename
    csv_path.parent.mkdir(parents=True, exist_ok=True)
    with open(csv_path, "w") as fh:
        fh.write("---\n")
        raw_csv = yaml.dump(
            csv, default_flow_style=False, allow_unicode=True, sort_keys=False
        )
        fh.write(annotate_image_tags(raw_csv, tag_map))
    print(f"  wrote {_display_path(csv_path)}")

    # metadata/annotations.yaml
    annotations = {
        "annotations": {
            "operators.operatorframework.io.bundle.channel.default.v1": channel,
            "operators.operatorframework.io.bundle.channels.v1": f"stable,{channel}",
            "operators.operatorframework.io.bundle.manifests.v1": "manifests/",
            "operators.operatorframework.io.bundle.mediatype.v1": "registry+v1",
            "operators.operatorframework.io.bundle.metadata.v1": "metadata/",
            "operators.operatorframework.io.bundle.package.v1": "stackable-secret-operator",
            "com.redhat.openshift.versions": openshift_versions,
        }
    }
    write_yaml(output_dir / "metadata" / "annotations.yaml", annotations)


if __name__ == "__main__":
    main()
