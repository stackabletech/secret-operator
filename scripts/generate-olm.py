def replace_image_refs(text: str, image_map: dict[str, str]) -> str:
    """Replace every occurrence of an OCI image ref (repo:tag) with its quay.io@digest form."""
    for src, dst in image_map.items():
        text = text.replace(src, dst)
    return text


def annotate_image_tags(text: str, tag_map: dict[str, str]) -> str:
    """
    Append a ``# <tag>`` comment to every ``image: <ref>`` line whose image
    reference appears in *tag_map* (mapping digest-form ref -> human tag).
    Existing trailing comments on those lines are left intact.
    """
    import re

    lines = text.splitlines(keepends=True)
    result = []
    for line in lines:
        m = re.match(r"^(\s*image:\s*)(\S+)(\s*)$", line.rstrip("\n"))
        if m:
            prefix, ref, _tail = m.group(1), m.group(2), m.group(3)
            # Strip surrounding quotes if present
            bare = ref.strip("\"'")
            if bare in tag_map:
                line = f"{prefix}{ref}  # {tag_map[bare]}\n"
        result.append(line)
    return "".join(result)


# Labels injected by Helm that should not appear in the OLM manifests.
_HELM_LABELS_TO_STRIP: frozenset[str] = frozenset(
    {
        "helm.sh/chart",
        "app.kubernetes.io/managed-by",
    }
)


def _strip_helm_labels_inplace(obj: object) -> None:
    """Recursively remove Helm-managed label keys from every 'labels' mapping in *obj*."""
    if isinstance(obj, dict):
        labels = obj.get("labels")
        if isinstance(labels, dict):
            for key in _HELM_LABELS_TO_STRIP:
                labels.pop(key, None)
        for value in obj.values():
            _strip_helm_labels_inplace(value)
    elif isinstance(obj, list):
        for item in obj:
            _strip_helm_labels_inplace(item)


def strip_helm_labels(doc: dict) -> dict:
    """Return a deep copy of *doc* with all Helm-managed labels removed."""
    import copy

    doc = copy.deepcopy(doc)
    _strip_helm_labels_inplace(doc)
    return doc


