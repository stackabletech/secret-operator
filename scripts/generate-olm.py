def build_configmap(
    helm_docs: list[dict], image_map: dict[str, str], tag_map: dict[str, str]
) -> dict:
    """
    Build the deployer ConfigMap whose entries are the operator's Kubernetes resources.
    OLM mounts this ConfigMap into the deployer Pod, which applies the resources.
    """
    data: dict[str, _LiteralStr] = {}

    for doc in helm_docs:
        kind = doc.get("kind", "")
        name = doc.get("metadata", {}).get("name", "")

        if kind == "SecurityContextConstraints":
            key = "stackable-secret-operator-scc.yaml"
        elif kind == "CSIDriver":
            key = "csidriver.yaml"
        elif kind == "StorageClass":
            key = "storageclass.yaml"
        elif kind == "Service" and name == "secret-operator":
            key = "service.yaml"
        elif kind == "DaemonSet":
            key = "daemonset.yaml"
        elif kind == "Deployment":
            key = "deployment.yaml"
        else:
            continue

        raw = yaml.dump(
            strip_helm_labels(doc),
            default_flow_style=False,
            allow_unicode=True,
            sort_keys=False,
        )
        raw = replace_image_refs(raw, image_map)
        raw = annotate_image_tags(raw, tag_map)
        data[key] = _LiteralStr(raw)

    return {
        "apiVersion": "v1",
        "kind": "ConfigMap",
        "metadata": {"name": "secret-operator-deployer-manifests"},
        "data": data,
    }


def _operator_cluster_rules(helm_docs: list[dict]) -> list[dict]:
    for doc in helm_docs:
        if doc.get("kind") == "ClusterRole":
            return doc.get("rules", [])
    return []


