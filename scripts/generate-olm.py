def get_quay_digest(quay_image: str, tag: str) -> str:
    """
    Return the content-addressable digest (sha256:...) for *quay_image*:*tag*
    by querying the quay.io Docker Registry HTTP API v2.
    """
    if not quay_image.startswith("quay.io/"):
        raise ValueError(f"Not a quay.io image reference: {quay_image!r}")
    repo = quay_image[len("quay.io/") :]
    url = f"https://quay.io/v2/{repo}/manifests/{tag}"
    req = urllib.request.Request(
        url,
        headers={
            "Accept": (
                "application/vnd.docker.distribution.manifest.list.v2+json,"
                "application/vnd.oci.image.index.v1+json,"
                "application/vnd.docker.distribution.manifest.v2+json,"
                "application/vnd.oci.image.manifest.v1+json"
            )
        },
    )
    try:
        with urllib.request.urlopen(req) as resp:
            digest = resp.headers.get("Docker-Content-Digest")
            if not digest:
                raise RuntimeError(
                    f"quay.io returned no 'Docker-Content-Digest' header for {quay_image}:{tag}"
                )
            return digest
    except urllib.error.HTTPError as exc:
        raise RuntimeError(
            f"Failed to fetch digest for {quay_image}:{tag}: HTTP {exc.code}"
        ) from exc


